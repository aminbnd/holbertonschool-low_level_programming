#include "3-calc.h"
/**
 * op_add - adds two integers
 * @a: first integer
 * @b: second integer
 * Return: a + b (integer)
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substructs an integer from another integer
 * @a: First integer (to substract from)
 * @b: Second integer (to substract)
 * Return: a - b (integer)
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two integers
 * @a: First integer
 * @b: second integer
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Divides two integers
 * @a:first inetegr
 * @b: second integer
 * Return: a / b (integer)
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulo a % b
 * @a: first integer
 * @b: second integer
 * Return: a % b (integer)
 */
int op_mod(int a, int b)
{
	return (a % b);
}
